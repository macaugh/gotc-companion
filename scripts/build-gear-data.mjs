#!/usr/bin/env node
// Reads gear-calc/*.csv and emits gear-data.generated.js.
// Filtered to kind == "standard" (Lords gear) for v1.

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const SRC  = resolve(ROOT, 'gear-calc');
const OUT  = resolve(ROOT, 'gear-data.generated.js');

const TIERS     = [1, 5, 10, 15, 20, 25, 30, 35, 40, 45];
const QUALITIES = ['poor','common','fine','exquisite','epic','legendary'];

// Minimal CSV parser supporting RFC-4180-style quoted fields.
function parseCSV(text) {
  const rows = [];
  let row = [], field = '', inQ = false, i = 0;
  while (i < text.length) {
    const c = text[i];
    if (inQ) {
      if (c === '"' && text[i+1] === '"') { field += '"'; i += 2; continue; }
      if (c === '"') { inQ = false; i++; continue; }
      field += c; i++; continue;
    }
    if (c === '"') { inQ = true; i++; continue; }
    if (c === ',') { row.push(field); field = ''; i++; continue; }
    if (c === '\n' || c === '\r') {
      if (c === '\r' && text[i+1] === '\n') i++;
      row.push(field); rows.push(row); row = []; field = ''; i++; continue;
    }
    field += c; i++;
  }
  if (field.length || row.length) { row.push(field); rows.push(row); }
  const header = rows.shift();
  return rows
    .filter(r => r.length === header.length)
    .map(r => Object.fromEntries(header.map((h, j) => [h, r[j]])));
}

function readCSV(name) {
  return parseCSV(readFileSync(resolve(SRC, name), 'utf8'));
}

// Lords gear is the base "standard" set + any event-themed Lords variants,
// which are identifiable by an `_lord<N>` suffix on the gear id. Dragon and
// trinket gear use different systems and are excluded.
const isLordsGear = (g) =>
  g.kind === 'standard' ||
  (g.kind && g.kind.startsWith('event') && /_lord\d+$/.test(g.id_name));

function main() {
  const gear     = readCSV('gear.csv').filter(isLordsGear);
  const recipes  = readCSV('recipes_summary.csv');
  const bonuses  = readCSV('gear_bonuses.csv');
  const slotsCsv = readCSV('equipment_slots.csv');
  const itemsCsv = readCSV('items.csv');

  const recipeByHash = new Map();
  for (const r of recipes) recipeByHash.set(r.gear_hash, r);

  const bonusesByHash = new Map();
  for (const b of bonuses) {
    if (!bonusesByHash.has(b.gear_hash)) bonusesByHash.set(b.gear_hash, []);
    let curve;
    try { curve = JSON.parse(b.curve_json); } catch { continue; }
    if (!Array.isArray(curve) || curve.length !== 6) continue;
    bonusesByHash.get(b.gear_hash).push({ prop: b.property_name, curve });
  }

  const SLOTS = {};
  const usedSlotIds = new Set(gear.map(g => g.slot));
  for (const s of slotsCsv) {
    if (usedSlotIds.has(s.id_name)) SLOTS[s.id_name] = s.name_key || s.id_name;
  }

  const MATERIALS = {};
  const GEAR = {};
  for (const g of gear) {
    const r = recipeByHash.get(g.hash);
    if (!r) continue;
    if (!r.tier || !r.time_sec) continue;
    let ingredients;
    try { ingredients = JSON.parse(r.ingredients_json); } catch { continue; }
    if (!Array.isArray(ingredients) || ingredients.length === 0) continue;

    const tier = Number(r.tier);
    if (!TIERS.includes(tier)) continue;

    GEAR[g.id_name] = {
      slot: g.slot,
      tier,
      recipe: {
        value: Number(r.value) || 0,
        time_sec: Number(r.time_sec),
        ingredients: ingredients.map(it => ({ mat: it.name, amount: Number(it.amount) })),
      },
      bonuses: bonusesByHash.get(g.hash) || [],
    };
    for (const it of ingredients) {
      if (!MATERIALS[it.name]) MATERIALS[it.name] = it.name; // label = id for v1
    }
  }

  // Backfill prettier material labels from items.csv when available.
  for (const it of itemsCsv) {
    if (MATERIALS[it.id_name] && it.name_key && !/^\s*$/.test(it.name_key)) {
      MATERIALS[it.id_name] = it.name_key;
    }
  }

  // Dedupe bonus curves and property names. With ~6000 pieces × several
  // bonuses each, but only a few hundred distinct curves and a few hundred
  // distinct property names, interning both tables shrinks the generated
  // file substantially. Each bonus becomes a two-int pair [propIdx, curveIdx]
  // to keep the payload compact.
  const curveIndex = new Map();
  const CURVES = [];
  function internCurve(curve) {
    const key = curve.join(',');
    let idx = curveIndex.get(key);
    if (idx === undefined) { idx = CURVES.length; CURVES.push(curve); curveIndex.set(key, idx); }
    return idx;
  }
  const propIndex = new Map();
  const PROPS = [];
  function internProp(name) {
    let idx = propIndex.get(name);
    if (idx === undefined) { idx = PROPS.length; PROPS.push(name); propIndex.set(name, idx); }
    return idx;
  }
  for (const piece of Object.values(GEAR)) {
    piece.bonuses = piece.bonuses.map(b => [internProp(b.prop), internCurve(b.curve)]);
  }

  const banner = `// AUTO-GENERATED by scripts/build-gear-data.mjs
// Source: gear-calc/*.csv (game static data dumps).
// Filtered to Lords gear (kind=standard + event _lordN variants).
// Do not hand-edit — regenerate.
//
// GEAR[id_name].bonuses[i] = [propIdx, curveIdx] — propIdx indexes PROPS,
// curveIdx indexes CURVES (the 6-value array, one per quality).\n`;

  // Compact JSON (no pretty-print) — this file is generated and read by code,
  // not humans, and pretty-printing inflates the file ~3x.
  const body =
    banner +
    `const TIERS = ${JSON.stringify(TIERS)};\n` +
    `const QUALITIES = ${JSON.stringify(QUALITIES)};\n` +
    `const SLOTS = ${JSON.stringify(SLOTS)};\n` +
    `const MATERIALS = ${JSON.stringify(MATERIALS)};\n` +
    `const PROPS = ${JSON.stringify(PROPS)};\n` +
    `const CURVES = ${JSON.stringify(CURVES)};\n` +
    `const GEAR = ${JSON.stringify(GEAR)};\n` +
    `(function (root, api) {
  if (typeof module === 'object' && module.exports) module.exports = api;
  else Object.assign(root, api);
}(typeof globalThis !== 'undefined' ? globalThis : this,
  { TIERS, QUALITIES, SLOTS, MATERIALS, PROPS, CURVES, GEAR }));\n`;

  writeFileSync(OUT, body);
  console.error(`wrote ${OUT} — ${Object.keys(GEAR).length} pieces, ${Object.keys(MATERIALS).length} materials, ${PROPS.length} props, ${CURVES.length} curves`);
}

main();
